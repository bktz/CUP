<h1>The Process: A guide on how the Community University Portal Works</h1>

<h3>1. Pitch an idea!</h3>
<p>The process starts when you, the community member/organization, starts an application for a project under the "pitch an idea" tab.
You will have to create an account on the site under the "Sign Up" feature and then be logged in to do this.</p>
<p>The application under "Pitch An Idea" requires a "project champion" to be identified.
This should be a specific person in an organization or for individuals it can be the same as the account creator. 
The champion should be available to contact to get more information about the project idea, and be involved throughout the project work.</p>
<p>You will be asked other questions about your project to help us understand your idea, fill them out to the best of your ability.</p>

<h3>2. Idea is reviewed</h3>
<p>Friendly members of the University Research Shop, the "Brokers", will now review your application and be in contact
with you to help guide you through the process of improving the application.</p>

<h3>3. Faculty takes on the idea</h3>
<p>After having been accepted by a broker projects Members of the university and the public can view the project and some of the details
that were provided in the application process.</p>
<p>Members of the university that are interested in contributing to your project can now apply to the project. Brokers will set up a meeting
with those persons and the project champion so both parties can understand more about the project and Begin working.</p>

<h3>4. Start work on the project</h3>
<p>Projects can be assigned goals that will display the state of the project as work between the community and the campus members goes along.</p>

<h3>5. Done!</h3>
<p>Projects that are completed will be marked accordingly and have a chance to be part of our project spotlight! (coming soon)</p>

